     */
    public function store(Request $request)
    {
        if (isset($_POST['ok'])) {
            $file = fopen('../data_feedback.txt', 'w+');
            $user = $request->input('user');
            $comment = $request->input('feedback');
            fwrite($file, $user . PHP_EOL . $comment . PHP_EOL);
            fclose($file);
            return Response::view('/feedback', ['status' => true]);
        }
        return redirect('feedback');
    }

