     */
    public function store(Request $request)
    {
        if (isset($_POST['ok']))
        {
            $file = fopen('../data_orders.txt','w+');
            $user = $_POST['user_orders'];
            $phone = $_POST['phone'];
            $email = $_POST['email'];
            $info = $_POST['info'];
            fwrite($file, $user . PHP_EOL . $phone . PHP_EOL . $email . PHP_EOL . $info . PHP_EOL);
            fclose($file);
        }
        return redirect('/order');
    }

